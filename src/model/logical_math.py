class LogicalMath:
    def logical_and(self, a: bool, b: bool) -> bool:
        return a and b

    def logical_or(self, a: bool, b: bool) -> bool:
        return a or b

    def logical_not(self, a: bool) -> bool:
        return not a

    def logical_xor(self, a: bool, b: bool) -> bool:
        return a ^ b